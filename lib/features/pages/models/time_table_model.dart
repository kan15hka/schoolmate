class TimeTablModel {
  String day;
  List<PeriodModel> periodsList;
  TimeTablModel({
    required this.day,
    required this.periodsList,
  });
  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['day'] = day;
    List<Map<String, dynamic>> jsonEventsList = [];
    for (PeriodModel element in periodsList) {
      jsonEventsList.add(element.toJson());
    }
    data['periodsList'] = jsonEventsList;
    return data;
  }
}

class PeriodModel {
  String periodType;
  String startTime;
  String endTime;

  String subjectName;
  String subjectCode;
  String subjectTeacher;
  int subjectCount;
  PeriodModel(
      {required this.periodType,
      required this.startTime,
      required this.endTime,
      required this.subjectCode,
      required this.subjectName,
      required this.subjectTeacher,
      required this.subjectCount});

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['periodType'] = periodType;
    data['startTime'] = startTime;
    data['endTime'] = endTime;
    data['subjectCount'] = subjectCount;
    data['subjectName'] = subjectName;
    data['subjectCode'] = subjectCode;
    data['subjectTeacher'] = subjectTeacher;
    return data;
  }
}

